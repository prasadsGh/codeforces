void dontquit()
{
    string a, b;
    cin >> a >> b;
    ll n = a.length(), m = b.length();
    ll dp[n + 1][m + 1];
    for (int i = 0; i <= m; i++)
        dp[0][i] = 0;
    for (int i = 0; i <= n; i++)
        dp[i][0] = 0;
    ll ans = 0;
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            if (a[i - 1] == b[j - 1])
                dp[i][j] = (1 + (dp[i - 1][j - 1]));
            else
                dp[i][j] = 0;
            ans = max(ans, dp[i][j]);
        }
    }
    cout << (n + m - (2 * ans)) << endl;
}
int main()
{
    ll t = 1;
    cin >> t;
    while (t--)
    {
        dontquit();
    }
    return 0;
}